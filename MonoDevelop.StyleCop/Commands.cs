namespace MonoDevelop.StyleCop
{
  #region Enumerations

  /// <summary>
  /// Enumerates available commands for Stylecop Addin
  /// </summary>
  public enum Commands
  {
    /// <summary>
    /// Run StyleCop on all files from solution
    /// </summary>
    RunStylecop,

    /// <summary>
    /// Run Stylecop on the active file
    /// </summary>
    RunStylecopOnFile
  }

  #endregion Enumerations
}